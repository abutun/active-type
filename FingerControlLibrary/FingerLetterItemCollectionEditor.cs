using System;
using System.Text;
using System.Collections;
using System.ComponentModel.Design;
using System.Reflection;

namespace ActiveType.FingerControlLibrary
{
    public class FingerItemCollectionEditor : System.ComponentModel.Design.CollectionEditor
    {
        private Type[] types;

        public FingerItemCollectionEditor(Type type) : base(type)
        {
            types = new Type[] { typeof(FingerLetterItem) };
        }

        protected override Type[] CreateNewItemTypes()
        {
            return types;
        }
    }
}
